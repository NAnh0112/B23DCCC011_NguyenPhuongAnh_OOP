package Quan_Ly_SV;

public class Student {
	private String ten;
	private int tuoi;
	public Student(String ten, int tuoi) {
		this.ten = ten;
		this.tuoi = tuoi;
	}
	public String getTen() {
		return ten;
	}
	public void setTen(String ten) {
		this.ten = ten;
	}
	public int getTuoi() {
		return tuoi;
	}
	public void setTuoi(int tuoi) {
		this.tuoi = tuoi;
	}
	
	
}
